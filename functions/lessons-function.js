document.getElementById('toggleButton_1').addEventListener('click', function() {
    const textSection_unit1 = document.getElementById('textSection_unit1');
    
    if (textSection_unit1.style.display === 'none' || textSection_unit1.style.display === '') {
        textSection_unit1.style.display = 'block';
    } else {
        textSection_unit1.style.display = 'none';
    }
});

